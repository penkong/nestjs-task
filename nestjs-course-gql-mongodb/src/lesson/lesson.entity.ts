import { Entity, PrimaryColumn, Column, ObjectIdColumn } from 'typeorm';
// this is for type orm and mongodb
@Entity()
export class Lesson {
  @ObjectIdColumn()
  _id: string;

  @PrimaryColumn()
  id: string;

  @Column()
  name: string;

  @Column()
  startDate: string;

  @Column()
  endDate: string;

  @Column()
  students: string[];
}
